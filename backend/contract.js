// Create provider
const provider = new ethers.JsonRpcProvider(FUJI_RPC_URL);

// Create contract instance
const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, provider);

// Helper function to create a signer (if private key is provided)
const getSigner = () => {
  if (process.env.ADMIN_PRIVATE_KEY) {
    return new ethers.Wallet(process.env.ADMIN_PRIVATE_KEY, provider);
  }
  return null;
};

// Helper function to retry contract calls with exponential backoff
const retryContractCall = async (contractMethod, maxRetries = 3) => {
  for (let i = 0; i < maxRetries; i++) {
    try {
      return await contractMethod();
    } catch (error) {
      console.error(`Contract call attempt ${i + 1} failed:`, error.message);
      if (i === maxRetries - 1) throw error;
      
      // Exponential backoff: wait 2^i seconds
      await new Promise(resolve => setTimeout(resolve, Math.pow(2, i) * 1000));
    }
  }
};

// Test connection on startup
const testConnection = async () => {
  try {
    const blockNumber = await provider.getBlockNumber();
    console.log(`✅ Connected to Avalanche Fuji - Block: ${blockNumber}`);
    
    // Test contract connection
    const candidates = await retryContractCall(() => contract.getCandidates());
    console.log(`✅ Contract connected - Candidates: ${candidates.join(', ')}`);
    
    return true;
  } catch (error) {
    console.error('❌ Connection test failed:', error.message);
    return false;
  }
};

// Initialize connection test
testConnection();

